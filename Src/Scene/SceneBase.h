#pragma once

namespace App {

    // ==========================================
    // SceneBase: 全シーンの基底クラス（抽象クラス）
    // 用途: シーンの共通インターフェースを定義
    // 派生: TitleScene, GameScene, ResultScene
    // ==========================================
    class SceneBase {
    public:
        // ==========================================
        // コンストラクタ・デストラクタ
        // ==========================================
        SceneBase() = default;

        // 純粋仮想デストラクタ（派生クラスで必ず実装）
        // inline定義でリンクエラーを回避
        virtual ~SceneBase() = 0;

        // ==========================================
        // シーンライフサイクル（純粋仮想関数）
        // 派生クラスで必ず実装する必要がある
        // ==========================================

        // 初期化: シーン開始時の設定
        // 呼び出しタイミング: シーン切り替え直後
        virtual void Init() = 0;

        // リソース読み込み: 画像・音声・シェーダーなど
        // 呼び出しタイミング: Init()の後
        virtual void Load() = 0;

        // 読み込み完了処理: リソース使用前の最終準備
        // 呼び出しタイミング: Load()の後
        virtual void LoadEnd() = 0;

        // 更新処理: ゲームロジック・入力処理
        // 呼び出しタイミング: 毎フレーム
        virtual void Update() = 0;

        // 描画処理: 画面への描画
        // 呼び出しタイミング: 毎フレーム（Update()の後）
        virtual void Draw() = 0;

        // 解放処理: リソースのクリーンアップ
        // 呼び出しタイミング: シーン終了時
        virtual void Release() = 0;
    };

    // デストラクタの定義（純粋仮想でもインライン定義が必要）
    inline SceneBase::~SceneBase() {}

} // namespace App