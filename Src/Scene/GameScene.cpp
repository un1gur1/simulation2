#include "GameScene.h"
#include <DxLib.h>
#include "../Input/InputManager.h" 
#include "SceneManager.h"

namespace App {

    // ==========================================
    // コンストラクタ: ゲームシーンの初期値設定
    // ==========================================
    GameScene::GameScene()
        : m_frameCount(0)      // フレームカウンター
        , m_showInfo(true)     // 情報表示フラグ（将来的な拡張用）
        , m_prevMouse(0)       // マウス状態保存（将来的な拡張用）
    {
    }

    // ==========================================
    // デストラクタ
    // ==========================================
    GameScene::~GameScene() {
    }

    // ==========================================
    // 初期化: バトル開始前の準備
    // シーン遷移時に毎回呼ばれる
    // ==========================================
    void GameScene::Init() {
        m_frameCount = 0;
        m_showInfo = true;
        m_battleMaster.Init();  // バトルマスターの初期化（ルール・ステージ設定など）
    }

    // ==========================================
    // リソース読み込み: 現在は非使用
    // 将来的にバトル用の画像・音声を読み込む場合に使用
    // ==========================================
    void GameScene::Load() {}

    // ==========================================
    // 読み込み完了処理: 現在は非使用
    // ==========================================
    void GameScene::LoadEnd() {}

    // ==========================================
    // 毎フレーム更新: ゲームロジックの進行
    // ==========================================
    void GameScene::Update() {
        // バトルマスターに処理を委譲
        // - ユニットの移動・攻撃
        // - AI思考
        // - ターン管理
        // - 勝敗判定
        // - リザルト画面への遷移
        // これら全てをBattleMasterが管理
        m_battleMaster.Update();

        ++m_frameCount;  // フレームカウント（デバッグ用）
    }

    // ==========================================
    // 描画: バトル画面の表示
    // ==========================================
    void GameScene::Draw() {
        // バトルマスターに描画を委譲
        // - マップグリッド
        // - プレイヤー・敵ユニット
        // - アイテム
        // - UI（ステータス、ログなど）
        m_battleMaster.Draw();
    }

    // ==========================================
    // 解放: リソースのクリーンアップ
    // シーン終了時に呼ばれる
    // ==========================================
    void GameScene::Release() {
        // 現在は特に解放処理なし
    }

} // namespace App