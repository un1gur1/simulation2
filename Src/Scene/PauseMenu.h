#pragma once
#include <DxLib.h>

namespace App {

    // ==========================================
    // PauseMenu: ポーズメニュー（ESCキーで表示）
    // 用途: ゲーム中断・再開・タイトル戻り・終了
    // ==========================================
    class PauseMenu {
    public:
        // ==========================================
        // Result: メニュー選択結果
        // ==========================================
        enum class Result {
            NONE,      // 選択なし（メニュー操作中）
            RESUME,    // ゲーム再開
            TITLE,     // タイトル画面へ
            EXIT       // ゲーム終了
        };

        // ==========================================
        // コンストラクタ
        // ==========================================
        PauseMenu();

        // ==========================================
        // 公開メソッド
        // ==========================================
        void Init();            // 初期化
        Result Update();        // 更新（戻り値: 選択結果）
        void Draw();            // 描画

    private:
        // ==========================================
        // メニュー状態
        // ==========================================
        int m_cursor;           // カーソル位置（0=再開, 1=タイトル, 2=終了）

        // ==========================================
        // 演出用
        // ==========================================
        float m_angle;          // 演算子アイコンの回転角度

        // ==========================================
        // 内部処理: 描画補助
        // ==========================================
        void DrawOperatorIcon(int x, int y, int size, unsigned int color);  // 演算子アイコン描画
    };

} // namespace App